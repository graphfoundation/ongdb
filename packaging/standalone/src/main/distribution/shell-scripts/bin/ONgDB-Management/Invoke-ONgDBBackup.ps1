<#
.SYNOPSIS
Invokes ONgDB Backup utility

.DESCRIPTION
Invokes ONgDB Backup utility

.PARAMETER CommandArgs
The remaining command line arguments to pass to the ONgDB Backup

.OUTPUTS
System.Int32
0 = Success
non-zero = an error occured

.NOTES
Only supported on version 1.x ONgDB Enterprise Edition databases

#>
function Invoke-ONgDBBackup
{
  [CmdletBinding(SupportsShouldProcess = $false,ConfirmImpact = 'Low')]
  param(
    [Parameter(Mandatory = $false,ValueFromRemainingArguments = $true)]
    [object[]]$CommandArgs = @()
  )

  begin
  {
  }

  process
  {
    try {
      return [int](Invoke-ONgDBUtility -Command 'Backup' -CommandArgs $CommandArgs -ErrorAction 'Stop')
    }
    catch {
      Write-Error $_
      return 1
    }
  }

  end
  {
  }
}
